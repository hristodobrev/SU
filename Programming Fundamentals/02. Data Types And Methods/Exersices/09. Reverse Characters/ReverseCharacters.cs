namespace _09.Reverse_Characters
{
    using System;

    class ReverseCharacters
    {
        static void Main()
        {
            char a = Console.ReadLine()[0];
            char b = Console.ReadLine()[0];
            char c = Console.ReadLine()[0];

            Console.Write(c);
            Console.Write(b);
            Console.WriteLine(a);
        }
    }
}
